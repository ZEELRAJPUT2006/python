# =====numeric datatype=======
a = 10
b = 10.25
c = 1j+5
print(type(a))
print(type(b))
print(type(c))

# ========= sequence datatype ========================
name = "hello"
print(type(name))
l = ["python","java","php"]
print(type(l))
t = ("python","java","hello","world","1","2")
print(type(t))

# ============= map =======================
s = {"python","a","b","c","a"}
print(s)
print(type(s))

d = {"python":"1","java": "2"}
print(type(d))

e = True
print(type(e))

# ============== casting ==================
a = 10
b = float(a)
print(type(b))
